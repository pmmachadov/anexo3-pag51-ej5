Crea una función llamada serialcheck que compruebe los números de serie de unos
productos que se componen de cuatro grupos de cinco cifras cada uno separados por
guiones:

02394-45677-30950-34503

La función recibe como parámetro una cadena y devuelve un valor lógico cierto o falso.
Para ello la función comprueba que la cadena dada contiene 4 grupos de cifras
separadas por guiones, que el primer y tercer valor son pares, y que el segundo y el
último son impares. En tal caso retorna como resultado un valor lógico cierto.